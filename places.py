dream_visit_places = ['France', 'Canada', 'Los Angeles', 'Great Britain', 'Russia']
print(dream_visit_places)

print("\nHere is the sorted list: ")
print(sorted(dream_visit_places))

print("\nThe original list is here: ")
print(dream_visit_places)

print("\nThe next sorted list is: ")
dream_visit_places.reverse()
print(dream_visit_places)

print("\nThe original list is still: ")
print(dream_visit_places)

print("\nThe reverse sorted list is here:")
dream_visit_places.sort(reverse=True)
print(dream_visit_places)
